public class NumberTable {
    public static void main(String[] args) {
        for (int i=0; i<10; i++) { /*  *//*  */
            System.out.println(); /*  */
            for (int j=1; j<=10; j++) { /*  */
                int k = (10*i)+j; /*  */
                System.out.print(k + " "); /*  */ }
        }    
        }
    }
